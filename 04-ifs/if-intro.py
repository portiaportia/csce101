# Let's learn about ifs
speed = float(input("Enter Speed: "))

if speed > 100:
    print("Do you even care about the rest of us?")
elif speed > 90:
    print("You should go to jail unless your in our class")
elif speed > 80:
    print("Please slow down, bad person")
elif speed > 70:
    print("you are a bad person")
elif speed > 50:
    print("You are somewhat good person")
elif speed < 45:
    print("You are bad for different reasons")
else:
    print("Thank you")

print("Goodbye")

